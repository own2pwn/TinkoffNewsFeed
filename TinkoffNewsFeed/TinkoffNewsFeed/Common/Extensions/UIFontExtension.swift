import UIKit

extension UIFont {
    static func helveticaBold(_ size: CGFloat) -> UIFont {
        let font = UIFont(name: "Helvetica Bold", size: size)!
        
        return font
    }
    
    static func helvetica(_ size: CGFloat) -> UIFont {
        let font = UIFont(name: "Helvetica", size: size)!
        
        return font
    }
    
    static func helveticaLight(_ size: CGFloat) -> UIFont {
        let font = UIFont(name: "Helvetica Light", size: size)!
        
        return font
    }
}
